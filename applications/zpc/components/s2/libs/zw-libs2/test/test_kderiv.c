void print_array(const char *name, uint8_t *arr, int len)
{
   int i;

   printf("%s", name);
   for ( i = 0; i < len; i++) {
       if (arr[i] <= 0xf)
           printf("0x0%x ", arr[i]);
       else
           printf("0x%x ", arr[i]);
   }
   printf("\n");
}

